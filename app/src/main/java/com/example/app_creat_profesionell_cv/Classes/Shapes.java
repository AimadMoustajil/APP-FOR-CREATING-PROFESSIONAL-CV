package com.example.app_creat_profesionell_cv.Classes;

public class Shapes {
    String image;
    String desc;

    public Shapes(String image, String desc) {
        this.image = image;
        this.desc = desc;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }
}
