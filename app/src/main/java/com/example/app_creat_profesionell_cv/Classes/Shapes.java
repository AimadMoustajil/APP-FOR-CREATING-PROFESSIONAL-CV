package com.example.app_creat_profesionell_cv.Classes;

public class Shapes {
    String image;
    int desc;

    public Shapes(String image, int desc) {
        this.image = image;
        this.desc = desc;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public int getDesc() {
        return desc;
    }

    public void setDesc(int desc) {
        this.desc = desc;
    }
}
